package sp18.dbms.group13.global.model;

public class Ingredients {

	String id;
	
	String name;
	
	String quantity;
	
	String calories;
	
	String ismeat;
	
	String isdairy;
	
	String hasgluten;
	
	String hasfat;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getCalories() {
		return calories;
	}

	public void setCalories(String calories) {
		this.calories = calories;
	}

	public String getIsmeat() {
		return ismeat;
	}

	public void setIsmeat(String ismeat) {
		this.ismeat = ismeat;
	}

	public String getIsdairy() {
		return isdairy;
	}

	public void setIsdairy(String isdairy) {
		this.isdairy = isdairy;
	}

	public String getHasgluten() {
		return hasgluten;
	}

	public void setHasgluten(String hasgluten) {
		this.hasgluten = hasgluten;
	}

	public String getHasfat() {
		return hasfat;
	}

	public void setHasfat(String hasfat) {
		this.hasfat = hasfat;
	}
	
	
}
