package sp18.dbms.group13.global.model;

public class Recipe {

	String searchString;
	
	String id;
	
	String description;
	
	String chefname;
	
	String preptime;
	
	String cooktime;
	
	String instructions;
	
	String cuisine;
	
	String rating;
	
	String num_of_reviews;
	
	String servings;
	
	String recipe_link;
	
	String keywords;
	
	String image_link;

	public String getSearchString() {
		return searchString;
	}

	public void setSearchString(String searchString) {
		this.searchString = searchString;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getChefname() {
		return chefname;
	}

	public void setChefname(String chefname) {
		this.chefname = chefname;
	}

	public String getPreptime() {
		return preptime;
	}

	public void setPreptime(String preptime) {
		this.preptime = preptime;
	}

	public String getCooktime() {
		return cooktime;
	}

	public void setCooktime(String cooktime) {
		this.cooktime = cooktime;
	}

	public String getInstructions() {
		return instructions;
	}

	public void setInstructions(String instructions) {
		this.instructions = instructions;
	}

	public String getCuisine() {
		return cuisine;
	}

	public void setCuisine(String cuisine) {
		this.cuisine = cuisine;
	}

	public String getRating() {
		return rating;
	}

	public void setRating(String rating) {
		this.rating = rating;
	}

	public String getNum_of_reviews() {
		return num_of_reviews;
	}

	public void setNum_of_reviews(String num_of_reviews) {
		this.num_of_reviews = num_of_reviews;
	}

	public String getServings() {
		return servings;
	}

	public void setServings(String servings) {
		this.servings = servings;
	}

	public String getRecipe_link() {
		return recipe_link;
	}

	public void setRecipe_link(String recipe_link) {
		this.recipe_link = recipe_link;
	}

	public String getKeywords() {
		return keywords;
	}

	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}

	public String getImage_link() {
		return image_link;
	}

	public void setImage_link(String image_link) {
		this.image_link = image_link;
	}
	
	
}
